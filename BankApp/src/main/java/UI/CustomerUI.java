    public void setCards()
    {
        
         modelx=(DefaultTableModel) jTableAccount.getModel();
         modely=(DefaultTableModel) jTableCredit.getModel();
         while (modelx.getRowCount()>0)
          {
             modelx.removeRow(0);
             
          }
         while (modely.getRowCount()>0)
          {
             modely.removeRow(0);
          }
         
      
         try {
            ArrayList<Card> cards = db.cardInfo(customer);
             customer.getCards().clear();
              
             for (Card card : cards) {
                 if(card.getCardType().equals("Bank"))
                 {
                     Object[] row ={card.getCardNum(),card.getCardName()};
                     modelx.addRow(row);
                     customer.addCard(card);
                 }
                 else if(card.getCardType().equals("Credit"))
                 {
                     Object[] row ={card.getCardNum(),card.getCardName()};
                     modely.addRow(row);
                     customer.addCard(card);
                 }
                 
             }
             
        }
        
         catch (Exception e) {
        } 
         
    }

       
