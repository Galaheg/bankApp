    private void btnCreditApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreditApplyActionPerformed
        if(creditType.equals("Marriage")||creditType.equals("Housing"))
        { Double x = Double.valueOf(txtCredit.getText());
            if(creditType.equals("Marriage")&&(customer.getIsMarried().equals("No")||customer.getIsMarried().equals("no")))
            {
                credit=w.Credit(x, customer,"Marriage");
                customer.addCredits(credit);
                setLbl();
            }
            else if(creditType.equals("Marriage"))
            {
                JOptionPane.showMessageDialog(null,"You are already married","Marriage",JOptionPane.ERROR_MESSAGE);
            }
            if (creditType.equals("Housing")&&(customer.getIsMarried().equals("Yes")||customer.getIsMarried().equals("yes"))){
                credit=w.Credit(x, customer,"Housing");
                customer.addCredits(credit);
                setLbl();
            }
            else if(creditType.equals("Housing"))
            {
                JOptionPane.showMessageDialog(null,"You are not married","Marriage",JOptionPane.ERROR_MESSAGE);
            }
        }
        else if(creditType.equals("Finance")||creditType.equals("Student"))
        {
            if(creditType.equals("Student")&&customer.getJob().equals("Student"))
            {
                credit=w.Credit(500.0,customer,"Student");
                customer.addCredits(credit);
                setLbl();
            }
            else if (creditType.equals("Student")){

                JOptionPane.showMessageDialog(null,"You are not Student","Student",JOptionPane.ERROR_MESSAGE);
            }
            if(creditType.equals("Finance"))
            {
                credit=w.Credit(2*customer.getIncome(), customer,"Finance");
                customer.addCredits(credit);
                setLbl();
            }

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnCreditApplyActionPerformed

